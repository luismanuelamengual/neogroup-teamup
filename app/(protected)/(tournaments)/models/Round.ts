import { BaseEntity, BelongsTo, BelongsToThrough, Column, Entity, HasMany } from '@neogroup/neorm'
import { Match } from '@/app/(protected)/(tournaments)/models/Match'
import { RoundStatus } from '@/app/(protected)/(tournaments)/models/RoundStatus'
import { RoundType } from '@/app/(protected)/(tournaments)/models/RoundType'
import { Tournament } from '@/app/(protected)/(tournaments)/models/Tournament'
import { TournamentCategory } from '@/app/(protected)/(tournaments)/models/TournamentCategory'

/** Optional per-round configuration stored as JSONB. */
export interface RoundSettings {
  /** Group index (0-based) for the group phase of a groups+playoff tournament. Null outside group rounds. */
  groupNumber?: number | null
}

@Entity({ table: 'rounds' })
export class Round extends BaseEntity {
  @Column({ primaryKey: true, autoGenerated: true })
  id!: number

  /** Category instance this round belongs to. */
  @Column({ cast: 'number' })
  tournamentCategoryId!: number

  @Column({ cast: 'number' })
  number!: number

  @Column({ cast: 'number' })
  status!: RoundStatus

  /** Kind of round (knockout / consolation / league / americano). */
  @Column({ cast: 'number' })
  type!: RoundType

  /**
   * Optional per-round configuration (JSONB). Currently holds:
   * - `groupNumber`: group index for the group phase of a groups+playoff tournament.
   */
  @Column({ cast: 'json' })
  settings!: RoundSettings | null

  /**
   * Whether this round is currently active (open and being played as part of
   * the tournament's current frontier). Several rounds can be active at once
   * (e.g. the groups of a groups+playoff, or a main + consolation bracket).
   * Replaces the former tournaments.currentRound counter.
   */
  @Column({ cast: 'boolean' })
  active!: boolean

  @Column({ cast: 'date' })
  createdAt!: Date

  /**
   * Convenience accessor for the group index stored in settings.
   * Reads/writes `settings.groupNumber` transparently so callers do not need
   * to know about the JSONB envelope.
   */
  get groupNumber(): number | null {
    return this.settings?.groupNumber ?? null
  }

  set groupNumber(value: number | null) {
    this.settings = { ...(this.settings ?? {}), groupNumber: value }
  }

  @BelongsTo(() => TournamentCategory, 'tournamentCategoryId')
  tournamentCategory?: TournamentCategory

  /** Shortcut to the tournament (through tournamentCategory). */
  @BelongsToThrough(() => Tournament, () => TournamentCategory, 'tournamentCategoryId', 'tournamentId')
  tournament?: Tournament

  @HasMany(() => Match, 'roundId')
  matches?: Match[]
}
