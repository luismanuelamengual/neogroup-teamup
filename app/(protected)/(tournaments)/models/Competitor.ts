import { BaseEntity, BelongsTo, BelongsToThrough, Column, Entity } from '@neogroup/neorm'
import { User } from '@/app/(auth)/models/User'
import { getUserDisplayName } from '@/app/(auth)/utils/user'
import { Tournament } from '@/app/(protected)/(tournaments)/models/Tournament'
import { TournamentCategory } from '@/app/(protected)/(tournaments)/models/TournamentCategory'

@Entity({ table: 'competitors' })
export class Competitor extends BaseEntity {
  @Column({ primaryKey: true, autoGenerated: true })
  id!: number

  /** Category instance this entry is registered in (implies the tournament). */
  @Column({ cast: 'number' })
  tournamentCategoryId!: number

  /** Platform user that registered this entry (null when added manually). */
  @Column()
  userId!: number | null

  /** Partner as a platform user (doubles disciplines only). */
  @Column()
  partnerUserId!: number | null

  /** Seed number for elimination-style tournaments (null when not seeded). */
  @Column({ cast: 'number' })
  seedNumber!: number | null

  @Column({ cast: 'date' })
  createdAt!: Date

  @BelongsTo(() => TournamentCategory, 'tournamentCategoryId')
  tournamentCategory?: TournamentCategory

  /** Shortcut to the tournament (through tournamentCategory). */
  @BelongsToThrough(() => Tournament, () => TournamentCategory, 'tournamentCategoryId', 'tournamentId')
  tournament?: Tournament

  @BelongsTo(() => User, 'userId')
  user?: User

  @BelongsTo(() => User, 'partnerUserId')
  partnerUser?: User

  /** Computed display name derived from related user/partnerUser (not stored in DB). */
  get displayName(): string {
    const userName = this.user ? getUserDisplayName(this.user) : null
    const partnerName = this.partnerUser ? getUserDisplayName(this.partnerUser) : null

    if (userName && partnerName) {
      return `${userName} / ${partnerName}`
    }

    return userName ?? ''
  }

  toJSON(): Record<string, any> {
    const result = super.toJSON()

    result.displayName = this.displayName

    return result
  }
}
