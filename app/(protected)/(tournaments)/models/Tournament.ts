import { BaseEntity, BelongsTo, Column, Entity, HasMany, HasManyThrough, HasOne } from '@neogroup/neorm'
import { RankingSettings } from '@/app/(protected)/(rankings)/models/RankingSettings'
import { Competitor } from '@/app/(protected)/(tournaments)/models/Competitor'
import { Discipline } from '@/app/(protected)/(tournaments)/models/Discipline'
import { Match } from '@/app/(protected)/(tournaments)/models/Match'
import { ScoreFormat } from '@/app/(protected)/(tournaments)/models/ScoreFormat'
import { SubDiscipline } from '@/app/(protected)/(tournaments)/models/SubDiscipline'
import { TournamentCategory } from '@/app/(protected)/(tournaments)/models/TournamentCategory'
import { TournamentImage } from '@/app/(protected)/(tournaments)/models/TournamentImage'
import { TournamentSettings } from '@/app/(protected)/(tournaments)/models/TournamentSettings'
import { TournamentStatus } from '@/app/(protected)/(tournaments)/models/TournamentStatus'
import { TournamentType } from '@/app/(protected)/(tournaments)/models/TournamentType'
import { OrganizationScope } from '@/app/models/OrganizationScope'
import { User } from '@/app/models/User'

@Entity({ table: 'tournaments' })
export class Tournament extends BaseEntity {
  @Column({ primaryKey: true, autoGenerated: true })
  id!: number

  @Column({ cast: 'number' })
  organizationId!: number

  @Column({ cast: 'number' })
  ownerId!: number

  @Column()
  name!: string

  @Column()
  description!: string | null

  @Column({ cast: 'number' })
  status!: TournamentStatus

  @Column({ cast: 'number' })
  discipline!: Discipline

  @Column()
  subDiscipline!: SubDiscipline | null

  @Column({ cast: 'number' })
  type!: TournamentType

  @Column({ cast: 'number' })
  scoreFormat!: ScoreFormat

  @Column()
  startDate!: string

  @Column()
  startTime!: string | null

  @Column()
  location!: string | null

  @Column({ cast: 'json' })
  settings!: TournamentSettings | null

  @Column({ cast: 'json' })
  rankingSettings!: RankingSettings | null

  /** When true, players must pay `entryFee` to register; otherwise the tournament is free. */
  @Column({ cast: 'boolean' })
  paid!: boolean

  /** Entry fee amount (in `currency`) required to register. Null/0 when the tournament is free. */
  @Column({ cast: 'number' })
  entryFee!: number | null

  /** ISO currency code of the entry fee (e.g. "ARS"). */
  @Column()
  currency!: string

  @Column({ cast: 'date' })
  createdAt!: Date

  @Column({ cast: 'date' })
  updatedAt!: Date

  @BelongsTo(() => User, 'ownerId')
  owner?: User

  @HasMany(() => TournamentCategory, 'tournamentId')
  categories?: TournamentCategory[]

  @HasOne(() => TournamentImage, 'tournamentId')
  image?: TournamentImage | null

  @HasManyThrough(() => Competitor, () => TournamentCategory, 'tournamentCategoryId', 'tournamentId')
  competitors?: Competitor[]

  @HasManyThrough(() => Match, () => TournamentCategory, 'tournamentCategoryId', 'tournamentId')
  matches?: Match[]

  protected static booted(): void {
    Tournament.addGlobalScope(new OrganizationScope())
  }
}
