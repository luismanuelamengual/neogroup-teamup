import { BaseEntity, Column, Entity } from '@neogroup/neorm'

@Entity({ table: 'organizations' })
export class Organization extends BaseEntity {
  @Column({ primaryKey: true, autoGenerated: true })
  id!: number

  @Column()
  name!: string

  /** Subdomain slug used to identify the organization (e.g. "club-aleman"). */
  @Column()
  domainName!: string

  @Column({ cast: 'date' })
  createdAt!: Date
}
